#include <stdio.h>

int suma(int a, int b)
{
    return a+b;
}

int resta(int a, int b)
{
    return a-b;
}

int multiplica(int a, int b)
{
    return a*b;
}

int divide(int a, int b)
{
    return a/b;
}

int mayor(int a, int b)
{
    if (a>b){
        return a;
    }
    else{
        return b;
    }
}
